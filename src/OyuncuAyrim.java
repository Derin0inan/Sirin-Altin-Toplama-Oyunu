
public class OyuncuAyrim {
	
	private int adim;

	private int azmanHasar;

	private int gargamelHasar;

	public OyuncuAyrim() {
		
	}

	public OyuncuAyrim(int adim, int azmanHasar, int gargamelHasar) {
		super();
		this.adim = adim;
		this.azmanHasar = azmanHasar;
		this.gargamelHasar = gargamelHasar;
	}

	public int getAdim() {
		return adim;
	}

	public int getAzmanHasar() {
		return azmanHasar;
	}

	public int getGargamelHasar() {
		return gargamelHasar;
	}

	public void setAdim(int adim) {
		this.adim = adim;
	}

	public void setAzmanHasar(int azmanHasar) {
		this.azmanHasar = azmanHasar;
	}

	public void setGargamelHasar(int gargamelHasar) {
		this.gargamelHasar = gargamelHasar;
	}
	
	
	
	
}
