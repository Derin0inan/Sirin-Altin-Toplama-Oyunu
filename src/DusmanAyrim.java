
public class DusmanAyrim {


	private int adim;
	private boolean atlarMi;

	public DusmanAyrim(int adim, boolean atlarMi) {
		super();
		this.adim = adim;
		this.atlarMi = atlarMi;
	}


	public int getAdim() {
		return adim;
	}

	public void setAdim(int adim) {
		this.adim = adim;
	}

	public void setAtlarMi(boolean atlarMi) {
		this.atlarMi = atlarMi;
	}
	public boolean isAtlarMi() {
		return atlarMi;
	}
	
}
